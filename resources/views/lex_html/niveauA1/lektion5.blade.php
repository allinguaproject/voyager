<h1>Regeln zur Pluralbildung</h1>

<h2>Pluralendung: -n/en</h2>

<table width="100%">
<tr class="männlich underline">
    <td class="bold" style="width=20%;">männlich</td> 
    <td style="width=80%;"> 
    Endung -e,- ent, -and, -ant, -ist, -or (schwache Deklination)</br>
    <span class="bold"> Beispiel: der Student – die Student<span class="text_yellow_background">en</span></span> 

    </td>

</tr>
<tr class="weiblich underline">
    <td class="bold" style="width=20%;">weiblich</td> 
    <td style="width=80%;"> 
    Feminine Nomen mit einer Silbe
    </br>
    <span class="bold"> Beispiel: die Frau – die Frau<span class="text_yellow_background">en</span></span> 
    </br>
    <span class="bold"> Beispiel: der Uhr – die Uhr<span class="text_yellow_background">en</span></span> 
    </br>

    Endung -e, -in, -ion, -ik, -heit, -keit, -schaft, -tät, -ung
    </br>
    <span class="bold"> Beispiel: der Nation – die Nation<span class="text_yellow_background">en</span></span> 
    </br>
    Besonderheit bei „-in“: wird „n“ verdoppelt 
    </br>
    <span class="bold"> Beispiel: der Lehrerin  – die Lehrerin<span class="text_yellow_background">nen</span></span> 
    


    </td>
    </tr>
    <tr class="sachlich underline">
        <td class="bold" style="width=20%;">sächlich</td> 
        <td>
        Bei Fremdwörtern mit der Endung -ma, -um, -us wird die Endung des Nomens
        im Plural meist durch en ersetzt. 
        </br>
        <span class="bold"> Beispiel: das Them<span class="text_yellow_background">a</span> – die Them<span class="text_yellow_background">en</span></span> 
        </td> 
    </tr>




</table>
